// Khai báo 3 biến với kiểu dữ liệu number
let number1: number;
let number2: number;
let result: number;

// Gán giá trị cho hai biến đầu tiên
number1 = 10;
number2 = 5;

// Thực hiện các phép tính toán cơ bản và gán kết quả cho biến thứ 3
result = number1 + number2;
console.log("Result of addition:", result);

result = number1 - number2;
console.log("Result of subtraction:", result);

result = number1 * number2;
console.log("Result of multiplication:", result);

result = number1 / number2;
console.log("Result of division:", result);

result = number1 % number2;
console.log("Remainder of division:", result);
